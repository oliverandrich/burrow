// newTestApp creates a configured App with a random key for testing.
func newTestApp(t *testing.T) *App {
	t.Helper()
	a := New()
	_ = a.Register(&burrow.AppConfig{
		Config: &burrow.Config{
			Server: burrow.ServerConfig{BaseURL: "http://localhost:8080"},
		},
	})
	require.NoError(t, a.configure("", false))
	return a
}

func TestCSRFTokenSetInContext(t *testing.T) {
	a := newTestApp(t)

	var token string
	inner := http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		token = Token(r.Context())
		w.WriteHeader(http.StatusOK)
	})

	mws := a.Middleware()
	require.Len(t, mws, 1)

	handler := mws[0](inner)

	req := httptest.NewRequestWithContext(t.Context(), http.MethodGet, "/", nil)
	rr := httptest.NewRecorder()
	handler.ServeHTTP(rr, req)

	assert.Equal(t, http.StatusOK, rr.Code)
	assert.NotEmpty(t, token, "CSRF token should be set in context")
}

func TestCSRFRejectsPostWithoutToken(t *testing.T) {
	a := newTestApp(t)

	inner := http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		w.WriteHeader(http.StatusOK)
	})

	handler := a.Middleware()[0](inner)

	req := httptest.NewRequestWithContext(t.Context(), http.MethodPost, "/", strings.NewReader("data=value"))
	req.Header.Set("Content-Type", "application/x-www-form-urlencoded")
	rr := httptest.NewRecorder()
	handler.ServeHTTP(rr, req)

	assert.Equal(t, http.StatusForbidden, rr.Code)
	body := rr.Body.String()
	assert.Equal(t, "Forbidden\n", body, "error response must not reveal CSRF failure reason")
}

func TestCSRFAcceptsPostWithToken(t *testing.T) {
	a := newTestApp(t)

	// First, do a GET to obtain the token and cookie.
	var token string
	inner := http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		token = gorillacsrf.Token(r)
		w.WriteHeader(http.StatusOK)
	})

	handler := a.Middleware()[0](inner)

	getReq := httptest.NewRequestWithContext(t.Context(), http.MethodGet, "/", nil)
	getRR := httptest.NewRecorder()
	handler.ServeHTTP(getRR, getReq)

	require.NotEmpty(t, token)

	// Extract cookies from GET response.
	cookies := getRR.Result().Cookies()

	// Now POST with the token in the header and the cookie.
	postReq := httptest.NewRequestWithContext(t.Context(), http.MethodPost, "/", strings.NewReader("data=value"))
	postReq.Header.Set("Content-Type", "application/x-www-form-urlencoded")
	postReq.Header.Set("X-CSRF-Token", token)
	for _, c := range cookies {
		postReq.AddCookie(c)
	}

	postRR := httptest.NewRecorder()
	handler.ServeHTTP(postRR, postReq)

	assert.Equal(t, http.StatusOK, postRR.Code)
}

