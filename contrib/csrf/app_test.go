func TestSecureDerivedFromBaseURL(t *testing.T) {
	tests := []struct {
		baseURL string
		secure  bool
	}{
		{"https://example.com", true},
		{"http://localhost:8080", false},
		{"http://example.com", false},
		{"https://localhost", true},
	}

	for _, tt := range tests {
		t.Run(tt.baseURL, func(t *testing.T) {
			a := New()
			_ = a.Register(&burrow.AppConfig{
				Config: &burrow.Config{
					Server: burrow.ServerConfig{BaseURL: tt.baseURL},
				},
			})
			err := a.configure("", tt.secure)
			require.NoError(t, err)
			// Verify the middleware was created (indirect check).
			assert.NotNil(t, a.protect)
		})
	}
}
